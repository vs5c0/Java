

class Literals {

     public static void main(String[] args) {
         
        // literals in java

        //binary
        int n = 0b101;
        System.out.println(n);
        //hexadecimal
        int v = 0x7;
        System.out.println(v);

        int c = 100_00_00_00;
        System.out.println(c);
        
        double cx = 56;
        System.out.println(cx);

        double ew = 512e6;
        System.out.println(ew);

        char r = 'v';
        r++;
        System.out.println(r);


        
    }
    
}
