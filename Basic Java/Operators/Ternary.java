package Operators;
class Ternary
{
    public static void main(String[] args) {
        int n  = 13;
        int res = 0;
          //Ternary Operators having 3 Operands it is a One line Replace ment of if -else

          res = (n%2==0)? 10: 11;
          System.out.println(res);
    }
}