package Strings.StrandMethods;

public class StrMethod3 {

    public static void main(String[] args) {
        
        //StartsWith() --> Used to find the whether the Passing Parameters are Start with that are Not?

        String s = "Java";
        System.out.println(s);
        System.out.println(s.startsWith("J"));

        //EndsWith() --> Used to find the whether the Pasing parameters are end with that are Not?

        System.out.println(s.endsWith("a"));

    }
    
}
