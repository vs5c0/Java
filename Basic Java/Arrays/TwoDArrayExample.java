package Arrays;

 class TwoDArrayExample {

    //Two Dimensional Arrays

 
    public static void main(String[] args) {
        
        int arr[][] = {{1,2,3},{3,4,5},{45,67,77}};
       

        for(int i=0;i<3;i++){
            for(int j=0;j<3;j++){
                System.out.print(arr[i][j]+ " ");
            }
        } System.out.println();
    }
    
}

    

