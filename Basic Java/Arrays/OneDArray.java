package Arrays;

 class OneDArray {
    //Single Dimensional Array 

    public static void main(String[] args) {
        
        int arr[] = new int[4];
        arr[0]=12;
        arr[1]=13;
        arr[2]=1;
        arr[3]=4;
       
        for(int i=0;i<arr.length;i++){
            System.out.println(arr[i]);
        }
    }
    
}
