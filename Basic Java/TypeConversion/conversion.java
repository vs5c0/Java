package TypeConversion;
 class conversion {
 public static void main(String[] args) {
       //Type conversion is a process of convert of one data type into another data type
       int a = 70;
       float b = a;
       System.out.println("Before Conversion:"+a); 
       System.out.println("After Conversion:"+b);

       byte v = 127;
       double m = v;
       System.out.println("Before Conversion:"+v); 
       System.out.println("After Conversion:"+m);
    }
}
