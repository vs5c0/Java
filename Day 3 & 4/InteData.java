class InteData
{
    public static void main(String[] args)
    {
        // Occupies 1 byte of memory
        byte age = 43;
        System.out.println("Age of a Person:"+age);
        // Occupies 2 bytes of memory
        short year = 2023;
        System.out.println("Present year:"+year);
        // Occupies 4 bytes of memory
        int sal = 43000000;
        System.out.println("Salary of a Person:"+sal);
        // Occupies 8 bytes  of memory
        long pop = 79128310129421l;
        System.out.println("Population in Berlin:"+pop);

    }
}