class floatDouble
{
    public static void main(String[] args)
    {
        // occupies 4 bytes of Memory
        float gravity = 9.8f;
        System.out.println("Gravity of Earth:"+gravity);
        
        // occupies 8 bytes of Memory
        double pi = 3.1435236192;
        System.out.println("Pi value:"+pi);
    }
}