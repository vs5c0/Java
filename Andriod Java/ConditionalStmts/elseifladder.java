package ConditionalStmts;

 class elseifladder {
    public static void main(String[] args) {
        
         int a = 34;

         if (a>35)
         {
            System.out.println("A is Greater than 34");
         } else if(a<34)
         {
            System.out.println("A is Less than 34");
         } else if(a==34)
         {
            System.out.println("A is Equal to 34");
         }
         else
         {
            System.out.println("A is not a Number ");
         }
    }
    
}
