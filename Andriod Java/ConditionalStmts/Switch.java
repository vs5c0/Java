package ConditionalStmts;

 class Switch {

    public static void main(String[] args) {
         int a = 1;

          switch(a)
          {
            case 1:
            System.out.println("A is One");
            break;

            case 2:
            System.out.println("A is Two");
            break;

            case 3:
            System.out.println("A is Three");
            break;

            default:
            System.out.println("A is not a 1, 2, 3");
            break;

          }
    }
    
}
