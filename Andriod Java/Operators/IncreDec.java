package Operators;

class IncreDec {
    public static void main(String[] args) {

    //Uniary Operator as Increment ++

    int a = 32;
    int c = 34;
    a++;
    ++c;
    System.out.println("Post Increment of A:" +a);
    System.out.println("Pre Increment of C:" +c);


//Decrement --

    int b = 32;
    int v = 43;
    b--;
    --v;
    System.out.println(" Post Decrement of B:" +b);
    System.out.println("Pre Decrement of V:" +v);
    
}
}